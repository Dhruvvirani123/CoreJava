package String;

public class Replace {
	public static void main(String[] args) {
		String s = "Dvruv Vinani";
		System.out.println(s);
		
		String reString = s.replace('v', 'h');
		System.out.println(reString);
		
		String s1 = "my name is Dhruv virani";
		String rep = s1.replace("is", "Was");
		System.out.println(rep);
	}
}
