package Learn;

public class Just_Example {
	public static void main(String[] args) {
		int i = 56;
		System.out.println(i++);
		System.out.println(i);
		System.out.println(++i);
		System.out.println(i);

		System.out.println();

		System.out.println(i--);
		System.out.println(i);
		System.out.println(--i);
		System.out.println(i);
	}
}